/* 鍵スケジュール用ラウンド定数 (FIPS 197 Table 5) */
static const uint8_t RCON[11] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36,
};

/* ------------------------------------------------------------------ */
/* 補助: ラウンド鍵を状態に XOR する                                    */
/* ------------------------------------------------------------------ */

static void xor_round_key(uint8_t st[16], const uint8_t *rk)
{
    for (int i = 0; i < 16; i++)
        st[i] ^= rk[i];
}

/* ------------------------------------------------------------------ */
/* SubBytes: 各バイトを S-box 値に置換する                              */
/* ------------------------------------------------------------------ */

static void sub_bytes(uint8_t st[16])
{
    for (int i = 0; i < 16; i++)
        st[i] = S[st[i]];
}

/* ------------------------------------------------------------------ */
/* ShiftRows: 各行をその行番号だけ左巡回シフトする                      */
/* 列優先配置: (行r, 列c) のバイト = st[c*4 + r]                       */
/* ------------------------------------------------------------------ */

static void shift_rows(uint8_t st[16])
{
    uint8_t tmp;

    /* 第1行: 左に1シフト */
    tmp = st[1]; st[1] = st[5]; st[5] = st[9]; st[9] = st[13]; st[13] = tmp;

    /* 第2行: 左に2シフト (対スワップ) */
    tmp = st[2]; st[2] = st[10]; st[10] = tmp;
    tmp = st[6]; st[6] = st[14]; st[14] = tmp;

    /* 第3行: 左に3シフト (= 右に1シフト) */
    tmp = st[15]; st[15] = st[11]; st[11] = st[7]; st[7] = st[3]; st[3] = tmp;
}

/* ------------------------------------------------------------------ */
/* MixColumns: GF(2^8) テーブルを使って各列を混合する                   */
/* 各列 (c): [2a+3b+c+d, a+2b+3c+d, a+b+2c+3d, 3a+b+c+2d]            */
/* ------------------------------------------------------------------ */

static void mix_columns(uint8_t st[16])
{
    for (int c = 0; c < 4; c++) {
        uint8_t a = st[c*4 + 0];
        uint8_t b = st[c*4 + 1];
        uint8_t cc = st[c*4 + 2];
        uint8_t d = st[c*4 + 3];

        st[c*4 + 0] = GF2[a] ^ GF3[b] ^ cc     ^ d;
        st[c*4 + 1] = a      ^ GF2[b] ^ GF3[cc] ^ d;
        st[c*4 + 2] = a      ^ b      ^ GF2[cc]  ^ GF3[d];
        st[c*4 + 3] = GF3[a] ^ b      ^ cc       ^ GF2[d];
    }
}

/* ------------------------------------------------------------------ */
/* 鍵スケジュール: FIPS 197 第5.2節                                     */
/* 11本 × 16バイトのラウンド鍵を ctx->sched に連続格納する             */
/* ------------------------------------------------------------------ */

void aes128_init(Aes128Ctx *ctx, const uint8_t key[AES128_KEY_LEN])
{
    uint8_t *sk = ctx->sched;

    /* 元の鍵をラウンド鍵0としてコピー */
    memcpy(sk, key, 16);

    for (int round = 1; round <= 10; round++) {
        const uint8_t *prev = sk + (round - 1) * 16;
        uint8_t       *cur  = sk +  round      * 16;

        /* 直前の鍵の末尾ワードに RotWord + SubWord + Rcon を適用 */
        uint8_t t0 = S[prev[13]] ^ RCON[round];
        uint8_t t1 = S[prev[14]];
        uint8_t t2 = S[prev[15]];
        uint8_t t3 = S[prev[12]];

        cur[ 0] = prev[ 0] ^ t0;
        cur[ 1] = prev[ 1] ^ t1;
        cur[ 2] = prev[ 2] ^ t2;
        cur[ 3] = prev[ 3] ^ t3;

        cur[ 4] = prev[ 4] ^ cur[ 0];
        cur[ 5] = prev[ 5] ^ cur[ 1];
        cur[ 6] = prev[ 6] ^ cur[ 2];
        cur[ 7] = prev[ 7] ^ cur[ 3];

        cur[ 8] = prev[ 8] ^ cur[ 4];
        cur[ 9] = prev[ 9] ^ cur[ 5];
        cur[10] = prev[10] ^ cur[ 6];
        cur[11] = prev[11] ^ cur[ 7];

        cur[12] = prev[12] ^ cur[ 8];
        cur[13] = prev[13] ^ cur[ 9];
        cur[14] = prev[14] ^ cur[10];
        cur[15] = prev[15] ^ cur[11];
    }
}

/* ------------------------------------------------------------------ */
/* 暗号化: FIPS 197 第5.1節                                             */
/* ------------------------------------------------------------------ */

void aes128_encrypt_block(const Aes128Ctx *ctx, uint8_t block[AES128_BLOCK_LEN])
{
    /*
     * FIPS 197 §3.4: 入力バイト i は state[r=i%4][c=i/4] に対応する。
     * 列優先フラット配列 st[c*4+r] におけるそのインデックスは c*4+r = i。
     * よって st[i] == block[i] — 転置は不要。
     */
    uint8_t st[16];
    memcpy(st, block, 16);

    xor_round_key(st, ctx->sched);

    for (int rnd = 1; rnd <= 9; rnd++) {
        sub_bytes(st);
        shift_rows(st);
        mix_columns(st);
        xor_round_key(st, ctx->sched + rnd * 16);
    }

    /* 最終ラウンド: MixColumns なし */
    sub_bytes(st);
    shift_rows(st);
    xor_round_key(st, ctx->sched + 10 * 16);

    memcpy(block, st, 16);
}
